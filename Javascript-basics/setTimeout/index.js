//setTimeout explain with example?
//The setTimeout() method calls a function or evaluates an expression after a specified number of milliseconds. The syntax for setTimeout() is:
//setTimeout(callbackFunction, delay, arg1, arg2, ...)
//The first argument is the callback function to be executed. The second argument is the delay in milliseconds before the function is executed. Any additional arguments are passed to the callback function as parameters.
//The setTimeout() method returns a unique identifier (ID) that can be used to cancel the timeout using the clearTimeout() method.
//Example:
// setTimeout(() => {
//     console.log('Hello, world!');
// }, 1000);
// // Outputs: Hello, world! after 1 second
