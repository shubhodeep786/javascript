// Expressions in JavaScript
// An expression is a statement that evaluates to a single value. It can consist of literals, variables, operators, or function calls.

// Arithmetic expressions
// Arithmetic expressions involve arithmetic operators (+, -, *, /, %) and operands.
// Example:
let result = 2 + 3; // Adds 2 and 3 together
let product = x * y; // Multiplies the values of x and y
let quotient = 10 / 2; // Divides 10 by 2

// Logical expressions
// Logical expressions involve logical operators (&&, ||, !) and Boolean values (true, false).
// Example:
let isTrue = true && false; // Evaluates to false
let isValid = x > 0 || y < 10; // Evaluates to true if either x is greater than 0 or y is less than 10
let isNotTrue = !true; // Evaluates to false

// Comparison expressions
// Comparison expressions involve comparison operators (==, ===, !=, !==, <, >, <=, >=) and operands.
// Example:
let isEqual = x == y; // Evaluates to true if x is equal to y
let isGreater = 10 > 5; // Evaluates to true
let isLessThanOrEqual = x <= 100; // Evaluates to true if x is less than or equal to 100

// Assignment expressions
// Assignment expressions involve assignment operators (=, +=, -=, *=, /=, %=) and operands.
// Example:
let a = 10; // Assigns the value 10 to variable a
let b = 5;
b += 5; // Adds 5 to the current value of b and assigns the result back to b
let c = 2;
c *= 2; // Multiplies the current value of c by 2 and assigns the result back to c

// Function call expressions
// Function call expressions involve calling a function with arguments.
// Example:
let squareRoot = Math.sqrt(16); // Calls the Math.sqrt() function with argument 16 and assigns the result to squareRoot
console.log('Hello, world!'); // Calls the console.log() function with the argument 'Hello, world!'
myFunction(x, y); // Calls the myFunction() function with arguments x and y

