//Expression in JavaScript is a statement that evaluates to a single value.
//Expressions can be literals, variables, operators, or function calls.

//Expressions in JavaScript can be categorized into several types, such as:
// // //Arithmetic expressions
// // //Arithmetic expressions involve arithmetic operators and operands. For example:
// // // 2 + 3
// // // x * y
// // // 10 / 2
// // //Logical expressions
// // //Logical expressions involve logical operators and Boolean values. For example:
// // // true && false
// // // x || y
// // // !true
// // //Comparison expressions
// // //Comparison expressions involve comparison operators and operands. For example:
// // // x == y
// // // 10 > 5
// // // x <= 100
// // //Assignment expressions
// // //Assignment expressions involve assignment operators and operands. For example:
// // // x = 10
// // // y += 5
// // // z *= 2
// // //Function call expressions
// // //Function call expressions involve calling a function with arguments. For example:
// // // Math.sqrt(16)
// // // console.log('Hello, world!')
// // // myFunction(x, y)

