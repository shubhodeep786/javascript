//Operators in JavaScript
//Operators are symbols that perform operations on operands. In JavaScript, operators can be classified into several categories, such as arithmetic operators, comparison operators, logical operators, assignment operators, and more. Here are some common operators in JavaScript:
// //Arithmetic operators
// //Arithmetic operators perform mathematical operations on numeric operands. Some common arithmetic operators in JavaScript include:
// // + (addition)
// // - (subtraction)
// // * (multiplication)
// // / (division)
// // % (modulus)
// // ++ (increment)
// // -- (decrement)
//
// //Comparison operators
// //Comparison operators are used to compare two values and return a Boolean result. Some common comparison operators in JavaScript include:
// // == (equal to)
// // === (strict equal to)
// // != (not equal to)
// // !== (strict not equal to)
// // > (greater than)
// // < (less than)
// // >= (greater than or equal to)
// // <= (less than or equal to)
//
// //Logical operators
// //Logical operators are used to combine or invert Boolean values. Some common logical operators in JavaScript include:
// // && (logical AND)
// // || (logical OR)
// // ! (logical NOT)
//
// //Assignment operators
// //Assignment operators are used to assign values to variables. Some common assignment operators in JavaScript include:
// // = (assignment)
// // += (addition assignment)
// // -= (subtraction assignment)
// // *= (multiplication assignment)
// // /= (division assignment)
// // %= (modulus assignment)
// // <<= (left shift assignment)
// // >>= (right shift assignment)
// // >>>= (unsigned right shift assignment)
// // &= (bitwise AND assignment)
// // ^= (bitwise XOR assignment)
// // |= (bitwise OR assignment)
