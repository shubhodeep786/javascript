// Document Object Model (DOM)
// The Document Object Model (DOM) is a programming interface for web documents, representing the page's structure, style, and content. It provides a tree-like structure of nodes and objects, allowing manipulation of the document through scripting languages like JavaScript.

// HTML lang attribute
// The lang attribute in HTML specifies the language of the document's content. It helps browsers and search engines understand the language used on the webpage.

// Difference between DOM and BOM
// The DOM (Document Object Model) is a standard for accessing documents and represents the structure of a web page. The BOM (Browser Object Model) represents the browser window and its components like history, location, navigator, etc.

// document.title
// The title property of the document object sets or returns the title of the current document.

// document.body
// The body property of the document object returns the body element of the document.

// document.body.children
// The children property of the body element returns a collection of child elements as an HTMLCollection object.

// document.getElementById()
// The getElementById() method returns the element with the specified ID.

// document.querySelector()
// The querySelector() method returns the first element in the document that matches the specified CSS selector.

// Handling multiple elements with the same ID
// If multiple elements have the same ID, getElementById() will return the first element encountered in the source code. IDs should be unique within a document.

// document.createElement()
// The createElement() method creates a new element node with the specified name.

// document.createTextNode()
// The createTextNode() method creates a new text node with the specified text.

// element.appendChild()
// The appendChild() method appends a node as the last child of a specified parent node.

// element.removeChild()
// The removeChild() method removes a specified child node from a parent node.

// element.innerHTML
// The innerHTML property sets or returns the HTML content (inner HTML) of an element.

// document.getElementsByClassName()
// The getElementsByClassName() method returns a collection of elements with the specified class name.

// document.getElementsByTagName()
// The getElementsByTagName() method returns a collection of elements with the specified tag name.

// document.getElementsByClassName().length
// The length property of getElementsByClassName() returns the number of elements in the collection.

// document.querySelector().length
// The length property of querySelector() returns the number of elements in the collection.

// document.getElementById().textContent
// The textContent property of getElementById() sets or returns the text content of the specified node.

// document.getElementByName()
// The getElementsByName() method returns a collection of elements with the specified name attribute value.

// Editing the Webpage with JavaScript

// document.title = "New Title"
// Changes the title of the document to "New Title".

// document.body.style.backgroundColor = "red"
// Sets the background color of the document body to red.

// document.getElementById("myElement").textContent = "Hello World!"
// Changes the text content of an element with the ID "myElement" to "Hello World!".

// document.getElementById("myElement").append("Hello World!")
// Appends "Hello World!" as a child node to the element with the ID "myElement".

