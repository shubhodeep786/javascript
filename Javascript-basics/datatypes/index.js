// Values in JavaScript

// Text Data
// Strings represent text data in JavaScript. They are sequences of characters enclosed in single, double, or backticks.
// Examples of strings:
// "hello"
// 'world'
// `JavaScript`
// "123" (even though it contains digits, it's still a string)

// Numbers in JavaScript
// Numbers represent numeric data in JavaScript. They can be integers or floating-point numbers.
// Examples of numbers:
// 42
// 3.14
// 0
// 1

// typeof() in JavaScript
// The typeof operator returns a string indicating the type of the operand.
// Example:
typeof ("hello"); // Returns: "string"
typeof (42); // Returns: "number"

// Primitive Data Types in JavaScript
// Primitive data types are basic data types in JavaScript that are immutable.
// There are six primitive data types: string, number, bigint, boolean, undefined, and symbol.
// Example of primitive data types:
// String: "hello"
// Number: 42
// Boolean: true
// Undefined: undefined
// null: null
// Symbol: Symbol("foo")

// Non-Primitive Data Types in JavaScript
// Non-primitive data types are complex data types that can hold multiple values and are mutable.
// Examples of non-primitive data types include objects, arrays, functions, and dates.
// Example of non-primitive data type:
const person = {
    name: "John",
    age: 30,
    isStudent: false
};
